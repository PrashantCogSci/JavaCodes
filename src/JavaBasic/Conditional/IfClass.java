package JavaBasic.Conditional;

public class IfClass {
    public static void main(String[] args) {
        int age =13;
        if (age < 18){
            System.out.println("age is Less then  18");
        }
    }
}


class Ifelse {
    public static void main(String[] args) {
        int age =13;
        if (age > 18){
            System.out.println("age is Less then  18");}
        else{
                System.out.println("age is greater then 18");
            }
        }
    }

//    Java Nested if statement


