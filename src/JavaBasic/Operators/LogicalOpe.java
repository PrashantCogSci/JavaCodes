package JavaBasic.Operators;


//Logical And Opertor

public class LogicalOpe {
    public static void main(String[] args) {
        int a = 12;
        int b = 20;
        int c = 30;

        if (c>a && c>b){
            System.out.println("c have greater value");
        }else{
            System.out.println("non of them ");
        }
    }
}


//Logical OR Opertor

 class LogicalOR {
    public static void main(String[] args) {
        int a = 12;
        int b = 20;
        int c = 30;

        if (a>c || c>b){
            System.out.println("c have greater value");
        }else{
            System.out.println("non of them ");
        }
    }
}





